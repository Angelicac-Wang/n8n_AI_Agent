#!/usr/bin/env python3
"""
Validate delete finetune dataset generated by generate_delete_training_dataset_v2.py.

Checks per-sample:
- assistant content is valid JSON workflow dict with nodes+connections
- target_node removed from nodes
- target_node not referenced anywhere in connections (any output type)
- all connection sources and targets refer to existing node names
- positional instructions have correct rank based on scanning ALL non-sticky node positions:
    left/right uses (x, y, name); top/bottom uses (y, x, name)

Also recomputes deletion on the provided Template and compares hashes to oracle_clues.jsonl.
"""

from __future__ import annotations

import argparse
import hashlib
import json
import re
from collections import Counter, defaultdict
from pathlib import Path
from typing import Any, Dict, Iterable, List, Optional, Tuple

JSONDict = Dict[str, Any]


def _sha256_text(s: str) -> str:
    return hashlib.sha256(s.encode("utf-8")).hexdigest()


def _sha256_json(obj: Any) -> str:
    s = json.dumps(obj, ensure_ascii=False, sort_keys=True, separators=(",", ":"))
    return hashlib.sha256(s.encode("utf-8")).hexdigest()


def _jsonl_iter(path: Path) -> Iterable[JSONDict]:
    with path.open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            yield json.loads(line)


def _is_sticky(node: JSONDict) -> bool:
    t = str(node.get("type") or "")
    return "stickynote" in t.lower()


def _workflow_node_names(wf: JSONDict) -> List[str]:
    nodes = wf.get("nodes")
    if not isinstance(nodes, list):
        return []
    out: List[str] = []
    for n in nodes:
        if isinstance(n, dict) and n.get("name"):
            out.append(str(n["name"]))
    return out


def _all_connection_refs(workflow: JSONDict) -> List[Tuple[str, str, str]]:
    conns = workflow.get("connections")
    if not isinstance(conns, dict):
        return []
    out: List[Tuple[str, str, str]] = []
    for src, outputs in conns.items():
        if not isinstance(outputs, dict):
            continue
        for out_type, out_lists in outputs.items():
            if not isinstance(out_lists, list):
                continue
            for targets in out_lists:
                if not isinstance(targets, list):
                    continue
                for t in targets:
                    if isinstance(t, dict) and t.get("node"):
                        out.append((str(src), str(out_type), str(t["node"])))
    return out


def _extract_template_from_user_content(user_content: str) -> Optional[JSONDict]:
    marker = "\n\nTemplate:\n"
    if marker not in user_content:
        return None
    _, tail = user_content.split(marker, 1)
    tail = tail.strip()
    try:
        obj = json.loads(tail)
        return obj if isinstance(obj, dict) else None
    except Exception:
        return None


def _rank_non_sticky(workflow: JSONDict) -> Dict[str, Dict[str, int]]:
    nodes = workflow.get("nodes")
    if not isinstance(nodes, list):
        return {}
    rows: List[Tuple[int, int, str]] = []
    for n in nodes:
        if not isinstance(n, dict) or _is_sticky(n):
            continue
        # rank only among deletable nodes (non-trigger) to match generator
        t = str(n.get("type") or "").lower()
        name = str(n.get("name") or "").lower()
        if "trigger" in t or t.endswith("trigger"):
            continue
        if t in {
            "n8n-nodes-base.manualtrigger",
            "n8n-nodes-base.webhook",
            "n8n-nodes-base.cron",
            "n8n-nodes-base.scheduletrigger",
            "n8n-nodes-base.formtrigger",
            "n8n-nodes-base.emailreadimap",
        }:
            continue
        if "webhook" in t or "cron" in t or "schedule" in t:
            continue
        if "on clicking" in name or "trigger" in name:
            continue
        name = n.get("name")
        pos = n.get("position")
        if not name or not isinstance(pos, list) or len(pos) < 2:
            continue
        try:
            x, y = int(pos[0]), int(pos[1])
        except Exception:
            continue
        rows.append((x, y, str(name)))
    if not rows:
        return {}
    lr = sorted(rows, key=lambda t: (t[0], t[1], t[2]))
    tb = sorted(rows, key=lambda t: (t[1], t[0], t[2]))
    names_lr = [nm for _x, _y, nm in lr]
    names_tb = [nm for _x, _y, nm in tb]
    ranks: Dict[str, Dict[str, int]] = {}
    for i, nm in enumerate(names_lr, 1):
        ranks.setdefault(nm, {})["from_left"] = i
        ranks[nm]["from_right"] = len(names_lr) - i + 1
    for i, nm in enumerate(names_tb, 1):
        ranks.setdefault(nm, {})["from_top"] = i
        ranks[nm]["from_bottom"] = len(names_tb) - i + 1
    return ranks


def _delete_node_strict(workflow: JSONDict, target_name: str) -> JSONDict:
    wf = json.loads(json.dumps(workflow, ensure_ascii=False))
    nodes = wf.get("nodes")
    if isinstance(nodes, list):
        wf["nodes"] = [n for n in nodes if not (isinstance(n, dict) and n.get("name") == target_name)]
    conns = wf.get("connections")
    if isinstance(conns, dict):
        conns.pop(target_name, None)
        for _src, outputs in list(conns.items()):
            if not isinstance(outputs, dict):
                continue
            for out_type, out_lists in list(outputs.items()):
                if not isinstance(out_lists, list):
                    continue
                for i, targets in enumerate(out_lists):
                    if not isinstance(targets, list):
                        continue
                    out_lists[i] = [t for t in targets if not (isinstance(t, dict) and t.get("node") == target_name)]
    return wf


# Match ordinals like "1st", "2nd", "3rd", "4th" in instructions.
ORDINAL_RE = re.compile(r"\b(\d+)(st|nd|rd|th)\b")


def _parse_ordinal_from_instruction(instr: str) -> Optional[int]:
    m = ORDINAL_RE.search(instr)
    if not m:
        return None
    try:
        return int(m.group(1))
    except Exception:
        return None


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--train-jsonl", type=str, required=True)
    ap.add_argument("--oracle-clues-jsonl", type=str, required=True)
    ap.add_argument("--limit", type=int, default=0)
    ap.add_argument("--debug-first-mismatch", action="store_true", help="Print details for the first positional rank mismatch")
    args = ap.parse_args()

    train_path = Path(args.train_jsonl)
    clues_path = Path(args.oracle_clues_jsonl)
    clue_by_sha: Dict[str, JSONDict] = {}
    for c in _jsonl_iter(clues_path):
        sha = c.get("input_sha256")
        if isinstance(sha, str):
            clue_by_sha[sha] = c

    counters = Counter()
    errors = Counter()
    by_variant = defaultdict(Counter)

    for i, r in enumerate(_jsonl_iter(train_path), 1):
        if args.limit and i > args.limit:
            break
        msgs = r.get("messages")
        if not isinstance(msgs, list) or len(msgs) < 2:
            errors["bad_messages"] += 1
            continue
        user = msgs[0].get("content") if isinstance(msgs[0], dict) else None
        assistant = msgs[1].get("content") if isinstance(msgs[1], dict) else None
        if not isinstance(user, str) or not isinstance(assistant, str):
            errors["missing_content"] += 1
            continue

        sha = _sha256_text(user)
        clue = clue_by_sha.get(sha)
        if not isinstance(clue, dict):
            errors["missing_clue"] += 1
            continue

        variant = str(clue.get("variant") or "")
        target = str(clue.get("target_node") or "")
        template_hash = str(clue.get("template_workflow_hash") or "")
        output_hash_expected = str(clue.get("output_workflow_hash") or "")

        counters["n"] += 1

        template = _extract_template_from_user_content(user)
        if not isinstance(template, dict):
            errors["template_parse_failed"] += 1
            by_variant[variant]["template_parse_failed"] += 1
            continue

        if _sha256_json(template) != template_hash:
            errors["template_hash_mismatch"] += 1
            by_variant[variant]["template_hash_mismatch"] += 1

        try:
            pred = json.loads(assistant)
        except Exception:
            errors["assistant_json_parse_failed"] += 1
            by_variant[variant]["assistant_json_parse_failed"] += 1
            continue
        if not isinstance(pred, dict) or not isinstance(pred.get("nodes"), list) or not isinstance(pred.get("connections"), dict):
            errors["assistant_shape_invalid"] += 1
            by_variant[variant]["assistant_shape_invalid"] += 1
            continue

        names = set(_workflow_node_names(pred))
        if target in names:
            errors["target_not_deleted"] += 1
            by_variant[variant]["target_not_deleted"] += 1

        # No references to target anywhere
        refs = _all_connection_refs(pred)
        if any(src == target or tgt == target for (src, _ot, tgt) in refs) or (target in (pred.get("connections") or {})):
            errors["target_referenced_in_connections"] += 1
            by_variant[variant]["target_referenced_in_connections"] += 1

        # All connection sources/targets exist as node names
        conns = pred.get("connections", {})
        bad = False
        if isinstance(conns, dict):
            for src, outputs in conns.items():
                if str(src) not in names:
                    bad = True
                    break
                if not isinstance(outputs, dict):
                    continue
                for _ot, out_lists in outputs.items():
                    if not isinstance(out_lists, list):
                        continue
                    for targets in out_lists:
                        if not isinstance(targets, list):
                            continue
                        for t in targets:
                            if isinstance(t, dict) and t.get("node") and str(t["node"]) not in names:
                                bad = True
                                break
        if bad:
            errors["dangling_connections"] += 1
            by_variant[variant]["dangling_connections"] += 1

        # Recompute strict deletion and compare output hash
        recomputed = _delete_node_strict(template, target)
        if _sha256_json(recomputed) != output_hash_expected:
            errors["oracle_output_hash_mismatch"] += 1
            by_variant[variant]["oracle_output_hash_mismatch"] += 1

        # Positional instruction rank check
        instr = str(clue.get("instruction") or "")
        ranks = _rank_non_sticky(template).get(target, {})
        ord_num = _parse_ordinal_from_instruction(instr)
        if variant in {"from_left", "from_right", "from_top", "from_bottom"}:
            expected = ranks.get(variant)
            if expected is None or ord_num is None or int(expected) != int(ord_num):
                errors["positional_rank_mismatch"] += 1
                by_variant[variant]["positional_rank_mismatch"] += 1
                if args.debug_first_mismatch:
                    # Provide a compact but actionable debug payload.
                    stored = {
                        "from_left": clue.get("rank_from_left"),
                        "from_right": clue.get("rank_from_right"),
                        "from_top": clue.get("rank_from_top"),
                        "from_bottom": clue.get("rank_from_bottom"),
                    }
                    # Recompute full ranks table size for context
                    full_ranks = _rank_non_sticky(template)
                    # Build a small ordering preview
                    nodes = template.get("nodes") if isinstance(template, dict) else None
                    rows: List[Tuple[int, int, str]] = []
                    if isinstance(nodes, list):
                        for n in nodes:
                            if not isinstance(n, dict) or _is_sticky(n):
                                continue
                            t = str(n.get("type") or "").lower()
                            nm_low = str(n.get("name") or "").lower()
                            if "trigger" in t or t.endswith("trigger"):
                                continue
                            if t in {
                                "n8n-nodes-base.manualtrigger",
                                "n8n-nodes-base.webhook",
                                "n8n-nodes-base.cron",
                                "n8n-nodes-base.scheduletrigger",
                                "n8n-nodes-base.formtrigger",
                                "n8n-nodes-base.emailreadimap",
                            }:
                                continue
                            if "webhook" in t or "cron" in t or "schedule" in t:
                                continue
                            if "on clicking" in nm_low or "trigger" in nm_low:
                                continue
                            nm = n.get("name")
                            pos = n.get("position")
                            if not nm or not isinstance(pos, list) or len(pos) < 2:
                                continue
                            try:
                                x, y = int(pos[0]), int(pos[1])
                            except Exception:
                                continue
                            rows.append((x, y, str(nm)))
                    lr = sorted(rows, key=lambda t: (t[0], t[1], t[2]))
                    tb = sorted(rows, key=lambda t: (t[1], t[0], t[2]))
                    payload = {
                        "sample_index": i,
                        "variant": variant,
                        "instruction": instr,
                        "parsed_ordinal": ord_num,
                        "expected_rank_recomputed": expected,
                        "stored_ranks_in_clue": stored,
                        "target_node": target,
                        "rank_table_size": len(full_ranks),
                        "left_right_first10": [nm for _x, _y, nm in lr[:10]],
                        "top_bottom_first10": [nm for _x, _y, nm in tb[:10]],
                    }
                    print(json.dumps(payload, ensure_ascii=False, indent=2))
                    return

        by_variant[variant]["n"] += 1

    report = {
        "n": int(counters["n"]),
        "errors": dict(errors),
        "by_variant": {k: dict(v) for k, v in by_variant.items()},
    }
    print(json.dumps(report, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()

